package functionl.math;

public class MathFuncion091 {
    public static int add(int add1, int add2) {
        return add1 + add2;
    }
    public static int subtract(int sub1, int sub2, int sub3, int sub4) {
        return sub1 - sub2 - sub3 - sub4;
    }
    public static double multiply(double mul1 ,double mul2 , double mul3) {
        return mul1 * mul2 * mul3;
    }
    public static double divide(double div1, double div2) {
        return div1 / div2;
    }
}
